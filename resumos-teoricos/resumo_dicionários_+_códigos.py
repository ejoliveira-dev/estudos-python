media = sum(salarios) / len(salarios) # sum() é pra somar todos os elementos

# exibe os funcionários abaixo da média
print("Abaixo da média:")
for i in range(10):
    if salarios[i] < media:
        print(f"{cpfs[i]} {nomes[i]}")

# exibe os funcionários acima da média
print("\nAcima da média:")
for i in range(10):
    if salarios[i] > media:
        print(f"{cpfs[i]} {nomes[i]}")