# D. Agrupando por letra.

# dicionário vazio; armazena os nomes agrupados pela primeira letra;
# a chave é a letra em maiúculo; e o valor será uma lista com os nomes correspondentes;
grupos = {}

while True:
    nome = input()
    if nome.lower() == "fim": # se o usuário digitar "fim" (maiúsculo ou minúsculo), encerra o loop
        break

    # pega a primeira letra e muda para maiúscula
    letra = nome[0].upper()

    if letra in grupos:
        grupos[letra].append(nome) # se existir, adiciona o nome na lista correspondente
    else:
        grupos[letra] = [nome] # se não, só cria uma nova lista

# imprime na ordem alfabética das letras
for letra in sorted(grupos.keys()): # ordena as letras
    print(f"{letra}: {' '.join(grupos[letra])}") # une os elementos de uma lista em string.

# E. Contagem de letras na frase.

# Escreva um programa que recebe uma única frase como entrada e conta quantas vezes cada letra do alfabeto aparece, ignorando espaços, acentos e letras maiúsculas/minúsculas.
# Exiba as letras e suas contagens em ordem alfabética.

frase = input()

# armazena a contagem das letras
contagem = {}

# percorre cada caractere
for char in frase:
    if char.isalpha():  # verifica se é uma letra (inclui acentos)
        letra = char.upper()   # transforma em maiúscula para padronizar
        if letra in contagem:
            contagem[letra] += 1
        else:
            contagem[letra] = 1

# ordena e imprime
for letra in sorted(contagem.keys()):
    print(f"{letra}: {contagem[letra]}")

